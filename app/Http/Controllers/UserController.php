    public function logout()
    {
        if (Auth::check()) {
            Auth::logout();
            return Redirect::to('login')->with('message','You are now logged out');
        } else {
            return Redirect::to('/');
        }
    }

    function is_email($x) {

        if (strpos($x, "@") !== false) {
            $split = explode("@", $x);
            if (strpos($split['1'], ".") !== false) { return true; }
            else { return false; }
        }
        else { return false; }

    }
